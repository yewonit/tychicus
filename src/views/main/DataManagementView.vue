        try {
          const newItem = await this.authCreateData(
            '/your-api-endpoint',
            this.editedItem,
            true
          );
          this.items.push(newItem);
          this.close();
        } catch (error) {
          console.error('Error adding new item: ', error);
        }
      },

      // 🚨 아이템 삭제 전 사용자에게 확인 요청
      async confirmDeletion(item) {
        if (confirm('Are you sure you want to delete this item?')) {
          await this.deleteSelectedItem(item);
        }
      },

      // 🗑️ 선택된 아이템을 서버에서 삭제 및 로컬 데이터 업데이트
      async deleteSelectedItem(item) {
        try {
          const result = await this.authDeleteData(
            '/your-api-endpoint',
            item.id,
            true
          );
          if (result) {
            const index = this.items.indexOf(item);
            this.items.splice(index, 1);
          }
        } catch (error) {
          console.error('Error deleting item: ', error);
        }
      },
    },
